    private void continueButtonTapped(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_continueButtonTapped
        /* Se n√£o selecionou nenhuma empresa */
        if(this.jTable1.getSelectedRowCount() == 0) {
            JOptionPane.showMessageDialog(null, "Selecione pelo menos um malote para continuar!", "Erro", JOptionPane.ERROR_MESSAGE);
        }
        /* Se selecionaram */
        else {
            
            /* Determina qual foi a escolha */
            int[] rows = this.jTable1.getSelectedRows();
            
            ArrayList<String[]> selectedPouchs = new ArrayList<String[]>();
            
            for(int i = 0; i < rows.length; i++) {
                selectedPouchs.add(this.databaseServices.getDatabase().get(rows[i]));
            }
            
            this.manager.changeCurrentPanel(new CreateTicketPanel(selectedPouchs));
            
            
            
        }
    }//GEN-LAST:event_continueButtonTapped


